// ── THEME ──
const themeToggle = document.getElementById('themeToggle');
const themeIcon   = document.querySelector('.theme-toggle-icon');
function setTheme(dark) {
  document.body.classList.toggle('light', !dark);
  if (themeIcon) themeIcon.textContent = dark ? '🌙' : '☀️';
  if (themeToggle) themeToggle.setAttribute('aria-label', dark ? 'Switch to light mode' : 'Switch to dark mode');
  localStorage.setItem('mobi-theme', dark ? 'dark' : 'light');
}
(function initTheme() {
  const saved = localStorage.getItem('mobi-theme');
  if (saved === 'light') { setTheme(false); return; }
  if (saved === 'dark')  { setTheme(true);  return; }
  setTheme(!window.matchMedia('(prefers-color-scheme: light)').matches);
})();
if (themeToggle) themeToggle.addEventListener('click', () => setTheme(document.body.classList.contains('light')));

// ── LANGUAGE ──
const langToggle   = document.getElementById('langToggle');
const langDropdown = document.getElementById('langDropdown');
const langFlag     = document.getElementById('langFlag');
const langCode     = document.getElementById('langCode');
const langItems    = Array.from(langDropdown.querySelectorAll('li'));

function applyLang(code) {
  const t = i18n[code];
  if (!t) return;

  // text nodes
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.dataset.i18n;
    if (t[key] !== undefined) el.innerHTML = t[key];
  });
  // placeholders
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const key = el.dataset.i18nPlaceholder;
    if (t[key] !== undefined) el.placeholder = t[key];
  });

  // update button
  const item = langItems.find(li => li.dataset.lang === code);
  if (item) {
    langFlag.textContent = item.dataset.flag;
    langCode.textContent = item.dataset.code;
  }

  // mark selected
  langItems.forEach(li => li.setAttribute('aria-selected', li.dataset.lang === code ? 'true' : 'false'));

  document.documentElement.lang = code;
  localStorage.setItem('mobi-lang', code);
}

function toggleDropdown(open) {
  langDropdown.classList.toggle('open', open);
  langToggle.setAttribute('aria-expanded', String(open));
}

langToggle.addEventListener('click', (e) => {
  e.stopPropagation();
  toggleDropdown(langDropdown.classList.contains('open') ? false : true);
});

langItems.forEach(li => {
  li.addEventListener('click', () => {
    applyLang(li.dataset.lang);
    toggleDropdown(false);
  });
});

document.addEventListener('click', () => toggleDropdown(false));
document.addEventListener('keydown', e => { if (e.key === 'Escape') toggleDropdown(false); });

// Init language
applyLang(localStorage.getItem('mobi-lang') || 'en');

// ── WAITLIST ──
const waitlistForm    = document.getElementById('waitlistForm');
const waitlistMessage = document.getElementById('waitlistMessage');
if (waitlistForm && waitlistMessage) {
  waitlistForm.addEventListener('submit', e => {
    e.preventDefault();
    const email = document.getElementById('waitlistEmail')?.value.trim();
    const lang  = localStorage.getItem('mobi-lang') || 'en';
    const msg   = (i18n[lang]?.success || i18n.en.success).replace('{email}', email || '');
    waitlistMessage.textContent = msg;
    waitlistForm.querySelector('.input-wrap').style.display = 'none';
  });
}

// ── FEATURE PANEL ──
const DURATION = 7000;
const pills    = Array.from(document.querySelectorAll('.feat-pill'));
const cards    = Array.from(document.querySelectorAll('.feat-card'));
const fillEls  = Array.from(document.querySelectorAll('.pill-bar-fill'));
let current = 0, fillStart = null, fillRaf = null, paused = false;

function showCard(index, resetTimer = true) {
  pills.forEach((p, i) => {
    p.setAttribute('aria-selected', 'false');
    fillEls[i].style.transition = 'none';
    fillEls[i].style.width = '0%';
  });
  cards.forEach(c => c.classList.remove('active'));
  current = index;
  pills[current].setAttribute('aria-selected', 'true');
  cards[current].classList.add('active');
  if (resetTimer) startTimer();
}

function startTimer() {
  cancelAnimationFrame(fillRaf);
  fillEls[current].style.transition = 'none';
  fillEls[current].style.width = '0%';
  fillStart = performance.now();
  function tick(now) {
    if (paused) { fillRaf = requestAnimationFrame(tick); return; }
    const pct = Math.min(((now - fillStart) / DURATION) * 100, 100);
    fillEls[current].style.width = pct + '%';
    pct < 100 ? (fillRaf = requestAnimationFrame(tick)) : advance();
  }
  fillRaf = requestAnimationFrame(tick);
}

function advance() { showCard((current + 1) % pills.length); }

pills.forEach((pill, i) => pill.addEventListener('click', () => showCard(i)));

const panel = document.querySelector('.features-panel');
if (panel) {
  panel.addEventListener('mouseenter', () => { paused = true; });
  panel.addEventListener('mouseleave', () => {
    paused = false;
    fillStart = performance.now() - (parseFloat(fillEls[current].style.width) / 100) * DURATION;
  });
}

showCard(0);

// Scroll reveal
const observer = new IntersectionObserver(entries => {
  entries.forEach(e => {
    if (e.isIntersecting) { e.target.style.opacity = '1'; e.target.style.transform = 'translateX(0)'; }
  });
}, { threshold: 0.1 });
pills.forEach((el, i) => {
  el.style.opacity = '0';
  el.style.transform = 'translateX(-14px)';
  el.style.transition = `opacity .4s ease ${i * 0.07}s, transform .4s ease ${i * 0.07}s`;
  observer.observe(el);
});
